#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	if (dstsize == 0)
		return (0);
	
	size_t	dst_len;
	size_t	i;

	i = 0;
	dst_len = ft_strlen(dst);
	while (i + dst_len < dstsize - 1 && src[i] != '\0')
	{
		dst[dst_len + i] = src[i];
		i++;
	}
	dst[dst_len + i] = '\0';
	return (dst_len + ft_strlen(src));
}

#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst)
		return ;
	while (lst != NULL)
	{
		f(lst->content);
		lst = lst->next;
	}
}

//ft_lstiter iterates the list lst and 
//applies the function f to the content of each node.
//ft_lstiter does not create a new list.